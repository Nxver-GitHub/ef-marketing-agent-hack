// ─── Type normalizers ────────────────────────────────────────────────────────
// Supabase rows use `id` (UUID string) and ISO timestamps.
// The mock store (and pages) use `_id` and millisecond timestamps.
type SupaRow = Record<string, unknown> & { id: string };
type SupaProspectRow = SupaRow & {
  created_at: string | number;
  updated_at: string | number;
};
type SupaSignalRow = SupaRow & {
  prospect_id: string;
  collected_at: string | number;
};
type SupaScoreRow = SupaRow & {
  prospect_id: string;
  computed_at: string | number;
  model_version?: string;
};
type SupaRunRow = SupaRow & {
  started_at: string | number;
  completed_at?: string | number | null;
};

const toP = (r: SupaProspectRow) => ({
  ...r,
  _id: r.id,
  created_at: +new Date(r.created_at),
  updated_at: +new Date(r.updated_at),
});
const toSig = (r: SupaSignalRow) => ({
  ...r,
  _id: r.id,
  collected_at: +new Date(r.collected_at),
});
const toScore = (r: SupaScoreRow) => ({
  ...r,
  _id: r.id,
  computed_at: +new Date(r.computed_at),
});
const toWeight = (r: SupaRow) => ({ ...r, _id: r.id });
const toRun = (r: SupaRunRow) => ({
  ...r,
  _id: r.id,
  started_at: +new Date(r.started_at),
  completed_at: r.completed_at ? +new Date(r.completed_at) : undefined,
});

// Stable empty references so consumers using `useMemo`/`useEffect` deps don't
// re-trigger when react-query is still loading.
const EMPTY_ARRAY: unknown[] = Object.freeze([]) as unknown[];
const EMPTY_RECORD: Record<string, unknown> = Object.freeze({}) as Record<string, unknown>;

// ─── Snapshot mode ────────────────────────────────────────────────────────────
// When VITE_USE_SNAPSHOT=true, every bulk read resolves from a one-shot JSON
// snapshot of the Supabase tables (src/lib/snapshot.json) instead of hitting
// PostgREST at runtime. Generated by scripts/snapshot-supabase.mjs. Demo-only:
// scoring writes still go through Supabase but won't be visible until you
// re-run the snapshot script.
const USE_SNAPSHOT =
  HAS_REAL_SUPABASE && (import.meta.env.VITE_USE_SNAPSHOT as string | undefined) === "true";

interface SnapshotShape {
  prospects: SupaProspectRow[];
  scores: SupaScoreRow[];
  signals: SupaSignalRow[];
  signal_weights: SupaRow[];
}

let snapshotPromise: Promise<SnapshotShape> | null = null;
function loadSnapshot(): Promise<SnapshotShape> {
  // Vite handles JSON imports natively; dynamic import lets us keep the
  // ~50MB blob out of the landing-page chunk and only pay the parse cost
  // when the user actually navigates to a route that needs it.
  if (!snapshotPromise) {
    snapshotPromise = import("./snapshot.json").then((m) => m.default as SnapshotShape);
  }
  return snapshotPromise;
}

// ─── Supabase hooks ───────────────────────────────────────────────────────────

// Supabase's REST endpoint defaults to a max of 1000 rows per request. Page
// through the result set so callers get every row regardless of table size.
const PAGE = 1000;
// `build` returns a Supabase query builder. We do not pin the chainable
// type because @supabase/supabase-js doesn't export it publicly and pinning
// would couple us to internals that change between releases.
// eslint-disable-next-line @typescript-eslint/no-explicit-any
async function fetchAllRows<T = SupaRow>(build: () => any): Promise<T[]> {
  const all: T[] = [];
  for (let from = 0; ; from += PAGE) {
    const { data, error } = await build().range(from, from + PAGE - 1);
    if (error) {
      console.error("[db] fetchAllRows error:", error);
      return all;
    }
    if (!data || data.length === 0) break;
    all.push(...(data as T[]));
    if (data.length < PAGE) break;
  }
  return all;
}

// All bulk Supabase queries share these defaults so multiple consumers of the
// same data hit a single in-memory cache instead of refetching independently.
// staleTime: pages won't refetch on remount within 5 min — kills the
// fan-out where /, /discover, /settings each paginated `prospects`/`scores`
// 11–21 times per load.
const BULK_STALE_MS = 5 * 60 * 1000;

function useSupaProspects() {
  const { data } = useQuery({
    queryKey: ["prospects", USE_SNAPSHOT ? "snapshot" : "live"],
    enabled: HAS_REAL_SUPABASE,
    staleTime: USE_SNAPSHOT ? Infinity : BULK_STALE_MS,
    queryFn: async () => {
      if (USE_SNAPSHOT) {
        const snap = await loadSnapshot();
        return snap.prospects.map(toP);
      }
      const rows = await fetchAllRows<SupaProspectRow>(() =>
        supabase!
          .from("prospects")
          .select("*")
          .order("created_at", { ascending: false })
          .order("id", { ascending: true }),
      );
      // Dedup by id: offset-pagination can return a boundary row on both
      // page N and N+1 when many rows share the same created_at.
      const seen = new Set<string>();
      const unique: SupaProspectRow[] = [];
      for (const r of rows) {
        if (seen.has(r.id)) continue;
        seen.add(r.id);
        unique.push(r);
      }
      return unique.map(toP);
    },
  });
  return data ?? EMPTY_ARRAY;
}

function useSupaProspect(id?: string) {
  const { data } = useQuery({
    queryKey: ["prospect", id, USE_SNAPSHOT ? "snapshot" : "live"],
    enabled: HAS_REAL_SUPABASE && !!id,
    staleTime: USE_SNAPSHOT ? Infinity : BULK_STALE_MS,
    queryFn: async () => {
      if (USE_SNAPSHOT) {
        const snap = await loadSnapshot();
        const row = snap.prospects.find((p) => p.id === id);
        return row ? toP(row) : null;
      }
      const { data: row } = await supabase!
        .from("prospects")
        .select("*")
        .eq("id", id!)
        .single();
      return row ? toP(row) : null;
    },
  });
  return data ?? null;
}

