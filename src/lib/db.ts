// Preference order when multiple scorer variants have written rows for the
// same prospect. Higher tier wins regardless of `computed_at`.
const MODEL_PRECEDENCE: Record<string, number> = {
  chartreuse_llm_v1: 1,
  chartreuse_deterministic_v1: 2,
  lightweight_v1: 3,
};
const pickBestScoreRow = <T extends { model_version?: string; computed_at: string | number }>(
  rows: T[],
): T | null => {
  if (!rows?.length) return null;
  const sorted = [...rows].sort((a, b) => {
    const ta = MODEL_PRECEDENCE[a.model_version ?? ""] ?? 9;
    const tb = MODEL_PRECEDENCE[b.model_version ?? ""] ?? 9;
    if (ta !== tb) return ta - tb;
    // same tier → newest first
    return +new Date(b.computed_at) - +new Date(a.computed_at);
  });
  return sorted[0];
};

function useSupaLatestScore(id?: string) {
  const [data, setData] = useState<ReturnType<typeof toScore> | null>(null);
  const timerRef = useRef<ReturnType<typeof setInterval> | null>(null);

  const fetch = useCallback(async () => {
    if (!id) return false;
    if (USE_SNAPSHOT) {
      // Snapshot mode: scores were already best-picked at snapshot time, so a
      // single lookup is enough — no polling needed (scoring writes won't be
      // visible until the snapshot script re-runs anyway).
      const snap = await loadSnapshot();
      const row = snap.scores.find((s) => s.prospect_id === id);
      if (row) {
        setData(toScore(row));
        return true;
      }
      return false;
    }
    // Pull every score row for this prospect; the in-memory ranker gives us
    // LLM > deterministic > lightweight precedence without a second query.
    const { data: rows } = await supabase!
      .from("scores")
      .select("*")
      .eq("prospect_id", id)
      .order("computed_at", { ascending: false });
    const best = pickBestScoreRow(rows ?? []);
    if (best) {
      setData(toScore(best));
      return true;
    }
    return false;
  }, [id]);

  useEffect(() => {
    if (!id) return;
    let active = true;
    fetch().then((found) => {
      if (!found && active && !USE_SNAPSHOT) {
        // Poll until the scoring run writes a score (up to 90 s)
        let attempts = 0;
        timerRef.current = setInterval(async () => {
          attempts++;
          const ok = await fetch();
          if (ok || attempts >= 90) clearInterval(timerRef.current!);
        }, 1000);
      }
    });
    return () => {
      active = false;
      if (timerRef.current) clearInterval(timerRef.current);
    };
  }, [id, fetch]);

  return data;
}

function useSupaLatestRun(id?: string) {
  const [data, setData] = useState<ReturnType<typeof toRun> | null>(null);
  const timerRef = useRef<ReturnType<typeof setInterval> | null>(null);

  const fetch = useCallback(async () => {
    if (!id) return null;
    // Snapshot mode never has a "running" scoring_runs row to poll —
    // the offline JSON is static. Skip the round trip entirely.
    if (USE_SNAPSHOT) return null;
    const { data: rows } = await supabase!
      .from("scoring_runs")
      .select("*")
      .eq("prospect_id", id)
      .order("started_at", { ascending: false })
      .limit(1);
    if (rows?.length) {
      setData(toRun(rows[0]));
      return rows[0];
    }
    return null;
  }, [id]);

  useEffect(() => {
    if (!id || USE_SNAPSHOT) return;
    fetch().then((run) => {
      if (run && (run.status === "running" || run.status === "pending")) {
        timerRef.current = setInterval(async () => {
          const r = await fetch();
          if (r?.status === "complete" || r?.status === "error") {
            clearInterval(timerRef.current!);
          }
        }, 500);
      }
    });
    return () => {
      if (timerRef.current) clearInterval(timerRef.current);
    };
  }, [id, fetch]);

  return data;
}

